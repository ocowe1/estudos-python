# Aula 4 - Tipos de dados primitivos

print('Vinicius', type('Vinicius'))
print(10, type(10))
print(25.23, type(25.23))
print('L' == 'L', type('L' == 'L'))

# Nome: String
print('Vinicius Costa Santos', type('Vinicius Costa Santos'))
# Idade: Int
print(22, type(22))
# Altura: Float
print(1.75, type(1.75))
# Maior de idade 10 > 20
print(22 > 18, type(22 > 18))
